int counter = 0;
while (counter < 10)
{
    Console.WriteLine(counter);
    counter++;
}
// O "while" serve para repetir um bloco de código enquanto uma condição for verdadeira.
counter = 0;
do
{
    Console.WriteLine(counter);
    counter++;
}
while (counter < 5);
// O "do while" serve para repetir um bloco de código pelo menos uma vez, enquanto uma condição for verdadeira.